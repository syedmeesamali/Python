def name_to_number(num):
    if num == "rock":
        return 0
    elif num == "Spock":
        return 1
    elif num == "paper":
        return 2
    elif num == "lizard":
        return 3
    elif num == "scissors":
        return 4
    else:
        return "ERROR!"

print(name_to_number("lizard"))

